                <div class="mb-10">
                    <h3 class="text-xl font-semibold mb-4">Benefits of global accounts for remote workers</h3>
                    <div class="mb-4">
                        <h6 class="text-base font-bold mb-4">1. Lower transaction costs</h6>
                        <p class="text-gray-600 leading-relaxed">
                            Global accounts typically offer more affordable transaction fees compared to

                            traditional banks. Many platforms allow you to receive payments from clients in

                            multiple countries without incurring hefty charges. Reduced or transparent

                            currency conversion fees also make it easier to budget and plan, helping remote

                            workers maximise their earnings.
                        </p>
                    </div>
                    <div class="mb-4">
                        <h6 class="text-base font-bold mb-4">2. Faster payment processing</h6>
                        <p class="text-gray-600 leading-relaxed">
                            With global accounts, payments from international clients are often processed

                            within hours or a couple of days, significantly faster than the lengthy timelines of

                            traditional banks. These accounts usually bypass multiple intermediary banks,

                            allowing funds to reach you more directly. Faster payments mean improved cash

                            flow, reduced financial stress, and greater flexibility in meeting personal or

                            business expenses on time.
                        </p>
                    </div>
                    <div class="mb-4">
                        <h6 class="text-base font-bold mb-4">3. Competitive exchange rates</h6>
                        <p class="text-gray-600 leading-relaxed">
                            Many global account providers offer exchange rates that are closer to the mid
                            market rate, helping you retain more value when converting currencies. Better rates

                            ensure you’re not unnecessarily losing money in conversion, allowing you to

                            reinvest in your work, save more effectively, and make the most of your

                            international earnings without hidden financial losses
                        </p>
                    </div>
                    <div class="mb-4">
                        <h6 class="text-base font-bold mb-4">4. Multi-currency flexibility</h6>
                        <p class="text-gray-600 leading-relaxed">
                            Global accounts let you hold, receive, and send funds in multiple currencies from a

                            single account. This is ideal for remote workers with clients across different

                            countries, as you can avoid unwanted conversions and manage your finances in

                            the currencies you prefer. Multi-currency access provides flexibility to spend or

                            save in a foreign currency when rates are most favourable, giving you more control

                            over your global income.
                        </p>
