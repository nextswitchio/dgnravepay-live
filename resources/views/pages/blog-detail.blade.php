                    <h3 class="text-xl font-semibold mb-4">Challenges remote workers face with traditional banks</h3>
                    <div class="mb-4">
                        <h6 class="text-base font-bold mb-4">1. High transaction fees</h6>
                        <p class="text-gray-600 leading-relaxed">
                            Traditional banks often charge significant fees for international transfers, including
                            both sender and receiver costs. These charges can reduce your earnings,
                            especially if you work with multiple clients abroad. Additional hidden fees for
                            currency conversion can further impact the amount you actually receive, making it
                            harder for remote workers to maximise their income from global clients.
                        </p>
                    </div>
                    <div class="mb-4">
                        <h6 class="text-base font-bold mb-4">2. Slow processing times</h6>
                        <p class="text-gray-600 leading-relaxed">
                            International payments via traditional banks can take several days or even weeks
                            to clear. Delays often occur due to intermediary banks, time zone differences, or
                            compliance checks. For remote workers who rely on timely payments to cover
                            expenses or reinvest in their work, this slow process can create financial
                            uncertainty and make budgeting far more challenging.
                        </p>
                    </div>
                    <div class="mb-4">
                        <h6 class="text-base font-bold mb-4">3. Unfavourable exchange rates</h6>
                        <p class="text-gray-600 leading-relaxed">
                            When receiving payments in a foreign currency, traditional banks often apply
                            exchange rates that are far less competitive than mid-market rates. This can lead
                            to remote workers losing a noticeable portion of their income during currency
                            conversion. Over time, these small losses add up, reducing the overall value of
                            international earnings.
                        </p>
                    </div>
